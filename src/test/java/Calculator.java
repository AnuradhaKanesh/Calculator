public class Calculator {

    public int getPercentageResult(int num1, int num2) {
        return (num1 * 100) / num2;
    }

    public int getMultiplicationResult(int num1, int num2) {
        return num1 * num2;
    }

    public int getSubtractionResult(int num1, int num2) {
        return num1 - num2;
    }

    public int getSquareResult(int num1) {
        return num1 * num1;
    }
}
